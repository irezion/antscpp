#include <iostream>
using namespace std;

int main() {
    int a;
    double b;
    float c;
    char d;


    a = 10;
    b = 5.5;
    c  = 3.2;
    d = 40;

    cout << "a + b = " << a + b <<  endl;
    cout << "b - c = " << b - c <<  endl;
    cout << "c * d = " << c * d <<  endl;
    cout << "d / a = " << d / a <<  endl;
    return 0;
}

